package FrameSettings;

/**
 * The Position helps us to know where our player is on the board
 */
public class Position {

    public int x;
    public int y;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public Position() {

        this.x = x;
        this.y = y;
    }
}
